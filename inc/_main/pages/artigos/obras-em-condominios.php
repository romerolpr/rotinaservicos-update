					<p>O post de hoje irá tratar sobre obras em condomínios. Abordaremos o tema em duas etapas: obras em áreas comuns e obras em áreas privativas, no interior das unidades habitacionais. De início, é de fundamental importância salientar que nenhuma obra pode comprometer a segurança do edifício. Deste modo, é de grande valia a contratação de profissional devidamente habilitado (arquiteto ou engenheiro), com inscrição regular em seu respectivo conselho de classe (CAU ou CREA).</p>

					<h3>Áreas comuns</h3>

					<p>Obras em condomínios residenciais são relativamente comuns, e englobam ações como conservação e manutenção, além de melhorias estéticas nas edificações. A legislação brasileira classifica obras em condomínios em três diferentes categorias: necessárias, úteis e voluptuárias.</p>

					<p>As obras necessárias são aquelas de caráter emergencial, em geral causadas por avarias em instalações elétricas ou hidráulicas, redes de gás, intempéries como descargas atmosféricas, ventos ou chuvas, entre outros. Podem ser realizadas independente de aprovação por Assembléia, pelo síndico ou, na omissão deste, por qualquer condômino.</p>

					<p>Obras úteis são aquelas que visam à melhora na utilização de equipamentos presentes no condomínio, como por exemplo, reforma de guarita da portaria (adaptações a fim de um ambiente mais seguro e/ou confortável), troca de fiação elétrica, adaptações para acesso mais fácil de veículos, etc. Ou seja, que tenham a finalidade de aumentar ou facilitar o uso de um determinado bem. Sua aprovação exige maioria absoluta em Assembléia.</p>

					<p>Já as voluptuárias referem-se à obras de menor necessidade, em geral voltadas à melhorias de aparência geral da edificação, como pintura ou troca de piso do playground infantil. Sua aprovação demanda dois terços em Assembléia. As definições nos termos da Lei podem ser consultadas no Código Civil (<a href="https://presrepublica.jusbrasil.com.br/legislacao/91577/codigo-civil-lei-10406-02" class="link" target="_blank" rel="nofollow">Lei 10.406/02</a>), em seu Art. 96, § 1º, § 2º e § 3º.</p>

					<p>Vale ressaltar que as interpretações sobre a classificação de cada obra podem variar, sendo sempre recomendável a procura por especialistas do ramo, sobretudo em obras de alto valor. A própria lei (Código Civil) trata deste tema em seu Art. 1.341, dizendo em sua redação que obras necessárias urgentes e de grande custo deverão ser comunicadas à assembléia, dando a ela devida ciência, enquanto que se necessárias, mas não urgentes, também de altos custos, dever-se-ão passar por aprovação de todos os condôminos. Deste modo, é fundamental que o síndico ou qualquer outro responsável busque sempre o consenso entre moradores, justificando sua necessidade, com transparência de custos e sempre buscando o auxílio de profissionais capacitados.</p>

					<h3>Áreas privativas</h3>

					<p>Em se tratando do interior das unidades habitacionais, alguns outros pontos devem ser observados. O síndico é aquele que tem como prerrogativa de função também a fiscalização de obras internas, sendo o responsável pela segurança de todo o prédio. É importante que as convenções coletivas abordem esse tema, e que sua divulgação seja eficaz, facilitando assim o trabalho do síndico e aumentado o conhecimento de moradores.</p>

					<p>Em geral, as principais demandas de obras internas abordam a remoção de paredes, envidraçamento de sacadas, instalações de novas louças sanitárias, troca de revestimentos de piso, parede ou teto, etc. Obras que podem parecer simples e inofensivas, mas, se feitas de modo indiscriminado, podem causar grandes transtornos ao condomínio e seus moradores.</p>

					<p>Quando falamos em unidades habitacionais recém-lançadas, é muito comum hoje em dia que os apartamentos sejam entregues “no osso”, sendo quase que obrigatório que o novo morador tenha que realizar outra obra subsequente, a fim de trazer conforto e boas condições de se habitar o imóvel. Neste caso, o atendimento pós-venda de construtoras, munidas de projetos “as built” da arquitetura, estrutura e instalações, além do contato com os responsáveis pela construção do edifício tornam essa tarefa menos complicada. Contudo, em se tratando de edifícios mais antigos, essa questão se torna mais complexa e a viabilidade da obra deve ser estudada com mais cuidado.</p>

					<p>Antes de se iniciar qualquer obra, é fundamental que o seu projeto de execução seja executado por profissional habilitado (arquiteto), pois fatores como sistema estrutural empregado, distribuição de instalações hidráulicas e elétricas e sobrecarga de laje devem ser minuciosamente estudados, a fim de se buscar a solução mais adequada. Um claro exemplo é a remoção de paredes em edifícios que se utilizam da alvenaria estrutural: a carga total da edificação está sustentada em seus blocos estruturais, e não em pilares, trazendo riscos até de desabamento do prédio em caso de sua indiscriminada remoção, mesmo que de pequenos trechos. Desta forma, estar com todos os projetos fornecidos pela construtora referentes à unidade habitacional em questão em mãos será de suma importância para o perfeito desenvolvimento da etapa de projeto e planejamento.</p>

					<p>Reformas devem contar com alvará da prefeitura, obrigando-se assim a apresentação de projeto e de termo de responsabilidade por parte do profissional responsável (RRT ou ART). A comunicação da obra deve obrigatoriamente ser feita ao síndico, que, por sua vez, deve estar atento aos desdobramentos das atividades, fiscalizando sempre sua correta execução e zelando pela segurança da edificação.</p>

					<p>Como dito acima, a comunicação com os moradores por parte do síndico é fundamental. Portanto, é extremamente válido que temas como horários para execução de obras, determinação de condições para movimentação de pessoas, equipamentos e materiais no interior do condomínio, uso do elevador, comunicação da obra e permissão para que o corpo diretivo visite os ambientes em obras constem na convenção coletiva e em espaços de divulgação internos, de modo que se faça todos saberem de suas responsabilidades.</p>

					
